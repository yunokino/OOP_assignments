#define		N	2000
#define		END_NUM	-999999

/* 允许添加最多一个自定义函数，不允许使用需要其他头文件的函数 */
int prime_num(int num)
{
	bool is_true=1;
	for (int i = 2; i < num - 1; i++)
	{
		if (num % i == 0)
		{
			is_true = 0;
			break;
		}
	}
	if (num < 2)
		is_true = 0;
	if (is_true)
		return 1;
	else
		return 0;
}
/***************************************************************************
  函数名称：
  功    能：向数组array中输入n个正整数，以-999999结束
  输入参数：
  返 回 值：
  说    明：input函数中不允许使用全局宏定义N
***************************************************************************/
void input(int* array, const int n)
{
	int* p;  //本函数只允许定义这一个指针变量，不再允许定义任何形式的变量
	p = array;
	for(;;array++)
	{	
		cin >> *array;
		if (*array == END_NUM)
			break;
	}
	array = p;
	/* 函数的实现部分，不允许任何形式的[]出现 */
}

/***************************************************************************
  函数名称：
  功    能：将数组array中的正整数依次输出，每n个一行，不包含最后的-999999
  输入参数：
  返 回 值：
  说    明：output函数中不允许使用全局宏定义N
***************************************************************************/
void output(int* array, const int n)
{
	int* p;  //本函数只允许定义这一个指针变量，不再允许定义任何形式的变量
	p = array;
	while (*array != END_NUM)
	{
		cout << *array<<" ";
		if (((array - p+1) % n == 0)&&array!=p)
			cout << endl;
		array++;
	}
	array = p;
	cout << endl;
	/* 函数的实现部分，不允许任何形式的[]出现 */
}

/***************************************************************************
  函数名称：
  功    能：数组顺序调整
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
void adjust(int* array)
{
	/* 除这三个变量外，本函数不再允许定义任何形式的变量
		 如果不用，允许删除，但不得增加或替换为其它形式的变量
		包括for(int i=0; )形式的新变量定义同样禁止 */
	int t, * p1, * p2;
	p1 = array;
	p2 = array;
	for (; *p1 != END_NUM; p1++)
	{
		for (; *p2 != END_NUM; p2++)
		{
			if ((prime_num(*p2) == 0) && prime_num(*(p2 + 1)) == 1)
			{
				t = *p2;
				*p2 = *(p2 + 1);
				*(p2 + 1) = t;
			}
		}
		p2 = array;
	}
	/* 函数的实现部分，不允许任何形式的[]出现，不允许使用需要其他头文件的函数 */
}

