#include <iostream>
#include<iomanip>
using namespace std;

int main()
{
	int n;
	int a[13][13] = { 0 };
	cout << "请输入要打印的行数[3..13]\n";
	cin >> n;
	cout << "--------------------------------------------------------------------------------\n";
	cout << "0         1         2         3         4         5         6         7\n";
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789\n";
	cout << "--------------------------------------------------------------------------------\n";
	for (int i = n - 1; i >= 0; i--)
	{
		for (int j = n - 1; j >= 0; j--)
		{
			if (j < i)
				break;
			if (j == n - 1 || i == j)
				a[i][j] = 1;
			else
				a[i][j] = a[i + 1][j] + a[i+1][j +1];
		}
	}
	for (int i = 0; i <= n - 1; i++)
	{
		for (int j = 0; j <= n - 1; j++)
		{
			if (a[i][j] == 0)
				cout << "      ";
			else
				cout << setw(6) << right << a[i][j];
		}
		cout << endl;
	}

	cout << "--------------------------------------------------------------------------------\n";
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789\n";
	cout << "0         1         2         3         4         5         6         7\n";
	cout << "--------------------------------------------------------------------------------\n";
	cout<<endl; 
	return 0;
}
